package ctest

import (
	"log"
	"os"
	"path/filepath"
)

// GetCurrentDirectory just return folder where executable has running
func GetCurrentDirectory() string {
	dir, err := filepath.Abs(filepath.Dir(os.Args[0]))
	if err != nil {
		log.Fatal(err)
	}
	return dir
}
